package com.zhyonk.entity;


public class DayVisitorCount {
	/**
	 * 日浏览量
	 */
	private Integer count;
	/**
	 * 日期
	 */
	private String day;
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
}
