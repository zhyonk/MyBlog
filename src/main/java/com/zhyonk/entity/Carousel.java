package com.zhyonk.entity;

public class Carousel {
	private int id;
	private String img_src;
	private String big_title;
	private String small_title;
	private String link;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getImg_src() {
		return img_src;
	}
	public void setImg_src(String img_src) {
		this.img_src = img_src;
	}
	public String getBig_title() {
		return big_title;
	}
	public void setBig_title(String big_title) {
		this.big_title = big_title;
	}
	public String getSmall_title() {
		return small_title;
	}
	public void setSmall_title(String small_title) {
		this.small_title = small_title;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	
	
}
